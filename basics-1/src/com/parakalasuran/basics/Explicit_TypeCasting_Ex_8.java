package com.parakalasuran.basics;

public class Explicit_TypeCasting_Ex_8 {

    public static void main(String[] args) {

        // int to byte

        int a = 257;

        byte b = (byte) a;  // explicit type casting

        System.out.println("b = " + b);


        double d1 = 1234.5678;

        int c1 = (int) d1;

        System.out.println("c1 = " + c1);

    }

}
